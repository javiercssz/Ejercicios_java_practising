public class tres_catorce {
public static void main(String[] args){
   int resultado;
    for(int num1 = 1; num1<=10; num1++){
       for(int num2 = 0; num2<=10; num2++){
           resultado = num1 * num2;
           System.out.println(num1 + "*" + num2 + "=" + resultado);
       }
   }

}
}
